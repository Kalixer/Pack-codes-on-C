
float additiones(float valueA, float valueB)
{
    return valueA + valueB;
}

float sustractiones(float valueA, float valueB)
{
    return valueA - valueB;
}

float multiplicationes(float valueA, float valueB)
{
    return valueA * valueB;
}

float divisiones(float valueA, float valueB)
{
    return valueA / valueB;
}
